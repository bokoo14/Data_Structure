#include "selectionSort.h"

void sort(int list[], int n) //매크로 SWAP을 사용하는 sort함수 정의
{
    int i, j, min, temp;
    for (i = 0; i < n - 1; i++)
    {
        min = i;
        for (j = i + 1; j < n; j++)
            if (list[j] < list[min])
                min = j;
        SWAP(list[i], list[min], temp);
    }
}